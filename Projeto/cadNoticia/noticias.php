        <h1>Casinhas para cães de rua instaladas no Centro de Friburgo</h1>
        <h3>Abrigos agora são de plástico, em substituição aos de papelão que ficavam molhados e duravam pouco</h3>
        <img src="https://avozdaserra.com.br/sites/default/files/noticias/casinha-caes_secom2_web_0.jpg">
        <p>Os cães comunitários que perambulam pelas ruas do Centro de Nova Friburgo agora têm casinhas para se abrigar da chuva e do frio. A prefeitura  instalou na última sexta-feira, 8,  três casinhas no canteiro próximo ao início da Avenida Alberto Braune. A ação é fruto de uma parceria com a PetFarma e visa à substituição das caixas de papelão nas quais os cães  se abrigavam no período de inverno. Segundo a prefeitura, esse projeto emergencial trará mais dignidade aos animais errantes e proteção contra as baixas temperaturas.

O local também recebeu diversas melhorias, como limpeza nivelamento do piso, poda e capina, e outras outras intervenções estão previstas. Uma placa informando monitoramento da área pelo Programa Cidade Inteligente e câmeras de outros estabelecimentos será instalada. Além de prevenir furto ou depredações das casinhas, as câmeras podem identificar tutores que abandonam animais nas proximidades do abrigo.</p>
